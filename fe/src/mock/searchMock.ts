export type DocumentItem = {
  id: string;
  title: string;
  orgName: string;
  domain: string;
  specialization: string;
  uploader: string;
  publicYear: number;
  isPremium: boolean;
  points?: number; // Ví dụ: điểm thưởng, hoặc giá tiền nếu là tài liệu premium
};

/** Helper function để tạo document nhanh chóng */
function doc(
  id: number,
  title: string,
  orgName: string,
  domain: string,
  specialization: string,
  uploader: string,
  publicYear: number,
  isPremium: boolean = false,
  points?: number,
): DocumentItem {
  return {
    id: String(id),
    title,
    orgName,
    domain,
    specialization,
    uploader,
    publicYear,
    isPremium,
    points: isPremium ? (points ?? 15000) : undefined,
  };
}

